numbers = [5,2,9,1,7]

numbers.append(10)
print("After appending 10:", numbers)

numbers.insert(2, 3)
print("After inserting 3 at index 2:", numbers)

numbers.remove(9)
print("After removing 9:", numbers)

numbers.sort()
print("After sorting:", numbers)

numbers.reverse()
print("After reversing:", numbers)

print('Lenght of the list:', len(numbers))